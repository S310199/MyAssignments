let number = 20

if(number%2 ==0)  //Condition
{
    console.log("The number is Even")   //Displayed when condition is True
}
else if(number%2 !=0)  //Condition
{
    console.log("\nThe number is Odd")   //Displayed when condition is True
}
else
{
    console.log("\nInvalid")  //Displayed when condition is False
}